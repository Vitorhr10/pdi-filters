   public void cinza(String cam) throws IOException {
        File arquivo = new File(cam);
        int w, h;

        BufferedImage original = ImageIO.read(arquivo);
        w = original.getWidth();
        h = original.getHeight();
        BufferedImage processada = new BufferedImage(w, h, BufferedImage.TYPE_INT_RGB);

        int[] pixels = new int[h * w * 3];
        WritableRaster raster = processada.getRaster();
        original.getRaster().getPixels(0, 0, w, h, pixels);

        int r, g, b;
        double fc;

        for (int i = 0; i < pixels.length; i += 3) {
            r = pixels[i];
            fc = r * 0.3;

            pixels[i] = (int) (fc);
        }
        raster.setPixels(0, 0, w, h, pixels);
        ImageIO.write(processada, "jpg", new File("redtestearvorecinza.jpg"));
        
        for (int i = 0; i < pixels.length; i += 3) {
            fc = 0;
            g = pixels[i];
            fc = g * 0.59;

            pixels[i + 1] = (int) (fc);
        }
        raster.setPixels(0, 0, w, h, pixels);
        ImageIO.write(processada, "jpg", new File("greentestearvorecinza.jpg"));
        
        for (int i = 0; i < pixels.length; i += 3) {
            fc = 0;
            b = pixels[i];
            fc = b * 0.11;

            pixels[i + 2] = (int) (fc);
        }
        raster.setPixels(0, 0, w, h, pixels);
        ImageIO.write(processada, "jpg", new File("bluetestearvorecinza.jpg"));
   }

